#打印0-10

count = 0
while count <= 10:
    print('loop',count)
    count += 1
print('----------------')

#打印0-10之间的奇数

count  = 0
while count <=10:
    if count % 2 == 1:
        print('loop',count)
    count += 1
print('----------------')


#打印0-10之间的偶数

count = 0
while count <= 10:
    if count % 2 == 0:
        print('loop',count)
    count += 1
